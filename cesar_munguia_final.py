def main():
    values = [4, 18, 12, 9, 2, 17, 24]

    total = 0
    for number in range(len(values)):
        total += values[number]

    mean = total / len(values)

    print()
    print(f'My list containing numbers: {values}')
    print(f'Mean: {mean:.3f}')
    print()
    print("Now to find the median we sort the list")
    values.sort()
    print(f'Sorted list: {values}')

    list_length = len(values)

    if list_length % 2 == 0:
        first_value = values[list_length//2]
        second_value = values[list_length//2 - 1]
        
        median = (first_value + second_value)/2

    else:
        median = values[list_length//2]

    print(f'The median of the list is {median}')
    print()

